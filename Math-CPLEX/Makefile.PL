use 5.000000;
use ExtUtils::MakeMaker;
use ExtUtils::Constant;

################################################################################
# make sure we know where the CPLEX files can be found
################################################################################
my $default_inc_dir = '/opt/ibm/ILOG/CPLEX_Studio125/cplex/include';
my $default_lib_dir = '/opt/ibm/ILOG/CPLEX_Studio125/cplex/lib/x86-64_sles10_4.1/static_pic';

print "\nMath::CPLEX needs access to the CPLEX header files and libraries, e.g.\n";
print "the CPLEX include directory might be: $default_inc_dir\n";
print "the CPLEX library directory might be: $default_lib_dir\n";

print "\nEnter CPLEX include directory: ";
my $cplex_inc_dir = <STDIN>;
chomp $cplex_inc_dir;
unless( -d $cplex_inc_dir )
{
   warn "WARNING: '$cplex_inc_dir' is not a valid directory on your system\n";
   warn "         going to use default directory '$default_inc_dir'\n";
   $cplex_inc_dir = $default_inc_dir;
}


print "\nEnter directory where CPLEX libraries (libcplex and libilocplex) can be found: ";
my $cplex_lib_dir = <STDIN>;
chomp $cplex_lib_dir;
unless( -d $cplex_lib_dir )
{
   warn "WARNING: '$cplex_lib_dir' is not a valid directory on your system\n";
   warn "         going to use default directory '$default_lib_dir'\n";
   $cplex_lib_dir = $default_lib_dir;
}
print "\n";
################################################################################


################################################################################
# create Makefile
################################################################################
WriteMakefile(
    NAME              => 'Math::CPLEX::Base',
    VERSION_FROM      => 'lib/Math/CPLEX/Base.pm', # finds $VERSION
    PREREQ_PM         => {}, # e.g., Module::Name => 1.1
    ($] >= 5.005 ?     ## Add these new keywords supported since 5.005
      (ABSTRACT_FROM  => 'lib/Math/CPLEX/Base.pm', # retrieve abstract from module
       AUTHOR         => 'Christian Jungreuthmayer <christian.jungreuthmayer@boku.ac.at>') : ()),
    LIBS              => ["-L$cplex_lib_dir -lm -lcplex -lilocplex -lpthread"], # e.g., '-lm'
    DEFINE            => '', # e.g., '-DHAVE_SOMETHING'
    INC               => "-I. -I$cplex_inc_dir", # e.g., '-I. -I/usr/include/other'
    # Un-comment this if you add C files to link with later:
    # OBJECT          => '$(O_FILES)', # link all the C files too
);
################################################################################

################################################################################
# write xs and c include files
################################################################################
